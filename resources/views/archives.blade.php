          <h1>Les archives départementales de Seine-Saint-Denis</h1>

          <h3>Inscription aux archives</h3>
            <p>Les Archives départementales sont un service public, accessible gratuitement à toute personne de nationalité française
            ou étrangère, majeure ou mineure, pour ces derniers, elles doivent être munie d’une autorisation parentale. Toute personne
            souhaitant consulter les collections s'inscrit au préalable comme lecteur, en produisant une pièce d'identité officielle en
            cours de validité comportant une photographie. Une carte de lecteur sera alors délivrée.</p>

          <h3>Conditions de communication</h3>
            <p>La consultation des documents s’effectue uniquement dans la salle de lecture des Archives départementales. La salle de lecture
            dispose de 26 places, 3 lecteurs de microfilms et de 4 postes informatiques.<br/>
            Communicabilité des documents La communication des archives publiques est régie par le Code du Patrimoine modifié par la loi
            du 15 juillet 2008 sur les archives. Les archives publiques sont communicables de plein droit.<br/>
            Toutefois, des délais ont été fixés pour certaines catégories de documents :</p>
            <ul>
              <li>25 ans* pour les documents dont la communication porte atteinte au secret des délibérations du Gouvernement et de l’exécutif
              en général</li>
              <li>50 ans* pour les documents qui contiennent des informations mettant en cause la sûreté de l’État ou la défense nationale et la
            vie privée des personnes (dossiers de pupilles, de naturalisation)</li>
              <li>50 ans à compter de la clôture du dossier de l’intéressé pour tout dossier de personnel ou 25 ans après la date de son décès</li>
              <li>75 ans* pour les documents relatifs aux recensements de population, les dossiers de procédures judiciaires, les minutes et
            répertoires des notaires ainsi que les registres des naissances et des mariages. Les registres des décès sont librement communicables</li>
              <li>100 ans à compter de la date du document le plus récent ou 25 ans à compter de la date du décès de l’intéressé pour les documents
            de nature à porter atteinte à la sécurité de personnes mineures, ou toutes personnes nommément désignées ou identifiables</li>
              <li>120 ans à compter de la date de naissance de l’intéressé pour les documents comportant des renseignements individuels à caractère
            médical ou 25 ans après la date de son décès</li>
          </ul>
            <p>* à compter de la date de l’acte ou de la clôture du dossier Une autorisation de consultation anticipée peut être accordée par
            le ministère de la culture à partir d’une demande de dérogation à adresser aux Archives départementales de la Seine-Saint-Denis.</p>
            <p>Les lecteurs peuvent solliciter une dérogation selon les règles strictes de procédure.</p>

            <h3>Condition de réservation des places :</h3>
              <p>Aucune</p>

            <h3>Condition de réservation du matériel :</h3>
              <p>Aucune</p>

            <h3>Conditions de recherche en salle de lecture :</h3>
              <p>Inventaires, répertoires et fichiers sont à la disposition du public en salle de lecture.<br/>
              Les présidents de salle accueillent, renseignent et guident le chercheur.</br>
              En aucun cas ils ne peuvent se substituer au chercheur pour effectuer sa recherche.</p>

            <h3>Demandes de documents :</h3>
              <p>Après inscription, le lecteur peut demander des documents en communication selon la réglementation en vigueur.</br>
              Si la fréquentation le permet, les demandes sont illimitées.</br>
              En période d’affluence, les présidents de salle peuvent limite la communication.</p>

            <h3>Délai d'obtention du document, selon sa nature :</h3>
              <p>Immédiat</p>

            <h3>Le nombre de documents consultables n'est pas déterminé :</h3>
              <p>Les documents sont rendus par les consultants eux-mêmes</p>

            <h3>Bibliothèque :</h3>
              <p>La bibliothèque d’étude et de recherche,rassemble la documentation historique relative au territoire francilien.</br>
              Elle compte plus de 3000 titres de périodiques et 18 000 documents consultables sur place.<p>

            <h3>Collections cartographiques et iconographiques :</h3>
              <p>Certains documents sont librement consultables en salle de lecture (cartes postales, cadastre,...).</br>
              D'autres sur le site internet <a href="http://archives.seine-saint-denis.fr" target="_blank">http://archives.seine-saint-denis.fr</a></p>

