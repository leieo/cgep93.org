              <p>La salle de lecture dispose de 26 places.</p>

            <h3>Microfilms :</h3>
              <p>3 lecteurs de microfilms permettent de visionner les microfilms de Seine Saint-Denis ou ceux provenant d'autres
              départements.</p>

            <h3>Reproduction :</h3>
